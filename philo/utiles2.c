#include "philo.h"

long	ft_atoi(char *str)
{
	int		i;
	long	k;
	long	value;

	i = 0;
	k = 1;
	value = 0;
	while ((str[i] >= 9 && str[i] <= 13) || str[i] == 32)
		i++;
	if (str[i] == '-')
		k = k * -1;
	if (str[i] == '+' || str[i] == '-')
		i++;
	while (str[i] >= '0' && str[i] <= '9')
	{
		value = (value * 10) + (str[i] - 48);
		i++;
	}
	// if ((value > INT_MAX) || (value < INT_MIN))
	// {
	// 	return (0);
	// }
	return (value * k);
}